public class OperadoresAritmeticos {
    public static void main(String[] args) {

        int a = 5, b =3;
        var resultado = 0;

        System.out.println(resultado = a + b);
        System.out.println(resultado = a - b);
        System.out.println(resultado = a * b);
        System.out.println(resultado = a / b);
        System.out.println(resultado = a % b);
       
    }
}
