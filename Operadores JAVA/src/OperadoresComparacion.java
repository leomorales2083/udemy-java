public class OperadoresComparacion {

    public static void main(String[] args) {
        int a = 4, b = 6;

        var resultado = a == b;
        System.out.println(resultado);

        resultado = a != b;
        System.out.println(resultado);

        resultado = a > b;
        System.out.println(resultado);

        resultado = a < b;
        System.out.println(resultado);

        resultado = a >= b;
        System.out.println(resultado);

        resultado = a <= b;
        System.out.println(resultado);      
        
    }
    
}
